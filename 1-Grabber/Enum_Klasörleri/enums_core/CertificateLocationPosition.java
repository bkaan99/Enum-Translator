package tr.com.fonet.core.common.enums;

import java.io.Serializable;
import tr.com.fonet.core.common.enums.base.DbEnum;

/**
 *
 * @author myalcinkaya
 */
public enum CertificateLocationPosition implements DbEnum {
    
    DB(1, "Database"),
    FOLDER(2, "Folder");
    
    private final Integer id;
    private final Serializable adi;
    
    CertificateLocationPosition(Integer id, Serializable adi) {
        this.id = id;
        this.adi = adi;
    }
    
    @Override
    public Serializable getAdi() {
        return adi;
    }
    
    @Override
    public Integer getId() {
        return id;
    }
}
